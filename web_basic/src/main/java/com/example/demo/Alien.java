package com.example.demo;

public class Alien {
	private int aid;
	private String aname;
	private String alang;
	public int getAid() {
		return aid;
	}
	public void setAid(int aid) {
		this.aid = aid;
	}
	public String getName() {
		return aname;
	}
	public void setName(String aname) {
		this.aname = aname;
	}
	public String getLang() {
		return alang;
	}
	public void setLang(String alang) {
		this.alang = alang;
	}
	

}
